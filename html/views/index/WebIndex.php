    <script>
    // Hàm mở modal profile
    function openProfile(staffId) {
        // Hiển thị modal
        document.getElementById('profileModal').style.display = 'block';
        
        // Hiển thị loading
        document.getElementById('profileContent').innerHTML = '<div class="loading">Đang tải thông tin...</div>';
        
        // Load nội dung profile bằng AJAX
        fetch('get_profile.php?id=' + staffId)
            .then(response => {
                if (!response.ok) {
                    throw new Error('Network response was not ok');
                }
                return response.text();
            })
            .then(data => {
                document.getElementById('profileContent').innerHTML = data;
            })
            .catch(error => {
                console.error('Error:', error);
                document.getElementById('profileContent').innerHTML = '<div class="loading"><p>Lỗi tải hồ sơ. Vui lòng thử lại.</p></div>';
            });
    }

    // Đóng modal khi click vào nút close
    document.querySelector('.close-profile').onclick = function() {
        document.getElementById('profileModal').style.display = 'none';
    }

    // Đóng modal khi click bên ngoài
    window.onclick = function(event) {
        const modal = document.getElementById('profileModal');
        if (event.target == modal) {
            modal.style.display = 'none';
        }
    }

    // Đóng modal bằng phím ESC
    document.addEventListener('keydown', function(event) {
        if (event.key === 'Escape') {
            document.getElementById('profileModal').style.display = 'none';
        }
    });
    </script>
